package cn.edu.guet.bean;

public class Water {
    private String buid;
    private double wprice;
    private int wnum;

  public String getBuid() {
    return buid;
  }

  public void setBuid(String buid) {
    this.buid = buid;
  }

  public double getWprice() {
    return wprice;
  }

  public void setWprice(double wprice) {
    this.wprice = wprice;
  }

  public int getWnum() {
    return wnum;
  }

  public void setWnum(int wnum) {
    this.wnum = wnum;
  }
}
